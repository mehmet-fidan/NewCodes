package loops;

public class _02_Example {
    public static void main(String[] args) {
        /*
        Question 2: find the sum of numbers starts with 1 and ends with 10.
        first and lat numbers must be included.
         */

        int sum =0;


        for (int i = 1; i <=10; i++) {
            sum+=i;

        } System.out.println("Sum of Numbers =" +sum);

    }
}
